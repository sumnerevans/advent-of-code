def solve(
    image: Set[Tuple[int, int]], enhance_algo: List[bool], n: int, test: bool = False
) -> int:
    min_r, max_r = seqminmax(x[0] for x in image)
    min_c, max_c = seqminmax(x[1] for x in image)

    for gen in range(n):
        new_image = set()
        for r in irange(min_r - 1, max_r + 1):
            for c in irange(min_c - 1, max_c + 1):
                x = []
                for ar, ac in grid_adjs((r, c), adjs_type=AdjacenciesType.ALL):
                    if test:
                        x.append((ar, ac) in image)
                    else:
                        if ar < min_r or ar > max_r:
                            x.append(gen % 2 == 1)
                        elif ac < min_c or ac > max_c:
                            x.append(gen % 2 == 1)
                        else:
                            x.append((ar, ac) in image)
                if enhance_algo[bitstrtoint(x)]:
                    new_image.add((r, c))

        image = new_image
        min_r -= 1
        min_c -= 1
        max_r += 1
        max_c += 1
    return len(image)


